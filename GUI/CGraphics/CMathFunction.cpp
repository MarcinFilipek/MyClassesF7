float CMathFunction::getSin(uint16_t alfa) {
	float sin = 0.0;
	uint16_t tempAngle = 0;
	tempAngle = alfa % 90;
	if ((alfa / 90 == 0) || ((alfa / 90) % 4 == 0)) {
		sin = m_sinTab[tempAngle];
	} else if ((alfa / 90) % 3 == 0) {
		sin = m_sinTab[90 - tempAngle] * (-1);
	} else if ((alfa / 90) % 2 == 0) {
		sin = m_sinTab[tempAngle] * (-1);
	} else if ((alfa / 90) % 1 == 0) {
		sin = m_sinTab[90 - tempAngle];
	}
	return sin;
}
